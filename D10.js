function newestMovie(movies) {
  if (movies.length === 0) {
    return null;
  }

  let newest = movies[0];

  for (let i = 1; i < movies.length; ++i) {
    const currentMovie = movies[i];

    if (currentMovie.Year > newest.Year) {
      newest = currentMovie;
    }
  }

  return newest;
}
